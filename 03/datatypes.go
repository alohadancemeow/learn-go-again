// 03: Datatypes

package main

import "fmt"

func main() {
	var age int = 30
	var price float64 = 19.99
	var name string = "GoLang"
	var isCool bool = true

	fmt.Println(age, price, name, isCool)
}

/**
Go has basic data types: int, float64, string, bool.
- int: Integer values
- float64: Decimal numbers
- string: Text
- bool: Boolean values
*/
